/// Corpus-driven byte-frequency table generator.
///
/// Usage:
///   cargo run --bin build_byte_freq -- [corpus_path] [output_path]
///
/// Defaults:
///   corpus_path  /mnt/g/repos/rust
///   output_path  stdout
///
/// To regenerate resharp-engine/src/simd/byte_freq.rs:
///   cargo run --bin build_byte_freq -- /mnt/g/repos/rust \
///       resharp-engine/src/simd/byte_freq.rs

use std::fs;
use std::io::{self, Write};
use std::path::Path;

// using rust compiler source for now
const DEFAULT_CORPUS: &str = "/mnt/g/repos/rust";

/// Directory names that are never source code.
const SKIP_DIRS: &[&str] = &[
    ".git", "target", "__pycache__", "node_modules", ".svn", ".hg",
];

/// File extensions whose content is binary and should not contribute to source
/// code byte statistics.
const SKIP_EXTENSIONS: &[&str] = &[
    // images
    "png", "jpg", "jpeg", "gif", "ico", "webp", "bmp", "tiff",
    // fonts
    "woff", "woff2", "ttf", "eot", "otf",
    // compiled / linked objects
    "exe", "dll", "so", "dylib", "a", "o", "rlib", "rmeta", "pdb", "d",
    // archives
    "gz", "zip", "tar", "zst", "xz", "bz2", "7z",
    // other binary
    "pyc", "db", "sqlite", "pdf", "class", "jar",
];

/// Maximum file size to scan (10 MiB). Protects against huge blobs in test
/// fixtures while keeping memory use bounded.
const MAX_FILE_BYTES: u64 = 10 * 1024 * 1024;

fn should_skip_dir(name: &str) -> bool {
    SKIP_DIRS.contains(&name)
}

fn should_skip_file(name: &str) -> bool {
    match name.rsplit('.').next() {
        Some(ext) => SKIP_EXTENSIONS.contains(&ext.to_ascii_lowercase().as_str()),
        None => false,
    }
}

fn walk(path: &Path, counts: &mut [u64; 256], total: &mut u64) -> io::Result<()> {
    if path.is_symlink() {
        return Ok(());
    }
    if path.is_file() {
        let name = path.file_name().and_then(|n| n.to_str()).unwrap_or("");
        if should_skip_file(name) {
            return Ok(());
        }
        if fs::metadata(path).map(|m| m.len()).unwrap_or(0) > MAX_FILE_BYTES {
            return Ok(());
        }
        match fs::read(path) {
            Ok(data) => {
                for &b in &data {
                    counts[b as usize] += 1;
                }
                *total += data.len() as u64;
            }
            Err(_) => {}
        }
    } else if path.is_dir() {
        let name = path.file_name().and_then(|n| n.to_str()).unwrap_or("");
        if should_skip_dir(name) {
            return Ok(());
        }
        // Sort entries for deterministic output across runs and platforms.
        let mut entries: Vec<_> = fs::read_dir(path)?
            .filter_map(|e| e.ok())
            .collect();
        entries.sort_by_key(|e| e.file_name());
        for entry in entries {
            walk(&entry.path(), counts, total)?;
        }
    }
    Ok(())
}

fn generate_source(counts: &[u64; 256]) -> String {
    let max_count = *counts.iter().max().unwrap_or(&1).max(&1);

    let mut table = [0u16; 256];
    for (b, &count) in counts.iter().enumerate() {
        if count > 0 {
            // Normalize to u16::MAX: most frequent byte → 65535, all others
            // proportionally scaled.  Bytes that appeared at least once get
            // at least 1 so that zero is unambiguously "not observed".
            let v = ((count * u16::MAX as u64 + max_count / 2) / max_count).max(1) as u16;
            table[b] = v;
        }
    }

    let mut out = String::with_capacity(6 * 1024);
    out.push_str(
        "// Auto-generated by `cargo run --bin build_byte_freq -- <corpus_path>`.\n\
         // Do not edit manually — re-run the binary to regenerate.\n\
         //\n\
         // Values are normalized byte frequencies: the most frequent byte in the\n\
         // corpus maps to u16::MAX (65535); all others are scaled proportionally.\n\
         // Higher value = more common = worse prefilter candidate.\n",
    );
    out.push_str("pub static BYTE_FREQ: [u16; 256] = [\n");
    for row in 0..16usize {
        let base = row * 16;
        out.push_str(&format!("    // 0x{:02X}..=0x{:02X}\n", base, base + 15));
        out.push_str("    ");
        for col in 0..16usize {
            let v = table[base + col];
            if col < 15 {
                out.push_str(&format!("{v:4},"));
            } else {
                out.push_str(&format!("{v:4},\n"));
            }
        }
    }
    out.push_str("];\n");
    out
}

fn main() -> io::Result<()> {
    let args: Vec<String> = std::env::args().collect();
    let corpus = args.get(1).map(|s| s.as_str()).unwrap_or(DEFAULT_CORPUS);
    let out_file = args.get(2).map(|s| s.as_str());

    let mut counts = [0u64; 256];
    let mut total: u64 = 0;

    eprintln!("Scanning {}...", corpus);
    walk(Path::new(corpus), &mut counts, &mut total)?;
    eprintln!("Total bytes scanned: {total}");

    let source = generate_source(&counts);

    if let Some(path) = out_file {
        fs::write(path, source.as_bytes())?;
        eprintln!("Written to {path}");
    } else {
        io::stdout().write_all(source.as_bytes())?;
    }

    Ok(())
}
